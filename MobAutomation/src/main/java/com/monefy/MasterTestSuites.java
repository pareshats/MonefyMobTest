package com.monefy;

public class MasterTestSuites {

	private String	Reference="";	
	
	private String	ApplicationReference="";	

	private String	Description="";

	private String	SerialNumber="";

	private String	ExecuteMasterTestSuite="";

	private String	ExecutionStatus="";
	
	private String	StartTime="";

	private String	EndTime="";
	

	public String getReference() {
		return Reference;
	}
	public void setReference(String reference) {
		Reference = reference;
	}
	
	public String getApplicationReference() {
		return ApplicationReference;
	}
	public void setApplicationReference(String applicationReference) {
		ApplicationReference = applicationReference;
	}
	
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	
	public String getSerialNumber() {
		return SerialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		SerialNumber = serialNumber;
	}
	
	
	public String getExecuteMasterTestSuite() {
		return ExecuteMasterTestSuite;
	}
	public void setExecuteMasterTestSuite(String executeMasterTestSuite) {
		ExecuteMasterTestSuite = executeMasterTestSuite;
	}
	
	public String getExecutionStatus() {
		return ExecutionStatus;
	}
	public void setExecutionStatus(String executionStatus) {
		ExecutionStatus = executionStatus;
	}
	
	public String getStartTime() {
		return StartTime;
	}
	public void setStartTime(String startTime) {
		StartTime = startTime;
	}
	
	public String getEndTime() {
		return EndTime;
	}
	public void setEndTime(String endTime) {
		EndTime = endTime;
	}
}
